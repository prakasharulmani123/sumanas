        function submitForm() {
            // Initiate Variables With Form Content
            var datastring = $("#contact_form").serialize();

            $.ajax({
                type: "POST",
                url: "submit-form.php",
                data: datastring,
//                dataType: "json",
                success: function (text) {
                    if (text == "success") {
                        formSuccess();
                    }
                }
            });
        }

        function formSuccess() {
            $("#msgSubmit").removeClass("hide");
            setTimeout(function () {
                $('#msgSubmit').addClass('hide');
            }, 2000);
        }

